	MES3 DB 10,13 , 'ENTER THE SIZE OF ARRAY:$'
	MES4 DB 10,13 , 'ENTER THE NUMBER TO BE SEARCHED:$'
	MES5 DB 10,13 , 'ELEMENT NOT FOUND$' 
Data ENDS


Code SEGMENT     
    ASSUME:DS:Data,CS:Code
    
START: 
    MOV AX,Data
    MOV DS,AX
        
    MOV AX,0
    
    MOV DX, OFFSET MES3
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H

	SUB AL,30H
	MOV LEN,AL  
	MOV CX,0
	MOV CL,AL
	
	MOV SI,0
	MOV SI, OFFSET ARR1

INPUT:
	MOV DX, OFFSET MES1
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H

	MOV ARR1[SI],AL
	INC SI
	DEC CX
	JNZ INPUT
	
	MOV DX, OFFSET MES4
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H

	MOV KEY,AL

SEARCH:
	MOV BL,0
	DEC LEN
	MOV DL,LEN
	MOV CL,KEY
	
LOOP1:
	CMP BL,DL
	JA FAIL

	MOV AL,BL
	ADD AL,DL
	SHR AL,1
	MOV AH,0
	MOV SI,AX
 	
	CMP CL, ARR1[SI]
	JAE BIG

	DEC AL
	MOV DL,AL
	
	JMP LOOP1
	
BIG:
	JE FOUND
	INC AL
	MOV BL,AL
	
	JMP LOOP1

FOUND:	
	MOV DX, OFFSET MES2
	MOV AH,09H
	INT 21H
	
	MOV DX,SI
	ADD DL,31H
	MOV AH,02H
	INT 21H
HLT

FAIL:	
	MOV DX, OFFSET MES5
	MOV AH,09H
	INT 21H

Code ENDS  
END START

